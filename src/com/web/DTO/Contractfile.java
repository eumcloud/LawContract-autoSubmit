package com.web.DTO;

public class Contractfile {
	private int no;
	private String creditoraddr;
	private String debtoraddr;
	private String contractfile;
	private String filepath;
	private String contractname;
	private String condition;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getCreditoraddr() {
		return creditoraddr;
	}
	public void setCreditoraddr(String creditoraddr) {
		this.creditoraddr = creditoraddr;
	}
	public String getDebtoraddr() {
		return debtoraddr;
	}
	public void setDebtoraddr(String debtoraddr) {
		this.debtoraddr = debtoraddr;
	}
	public String getContractfile() {
		return contractfile;
	}
	public void setContractfile(String contractfile) {
		this.contractfile = contractfile;
	}
	public String getFilepath() {
		return filepath;
	}
	public void setFilepath(String filepath) {
		this.filepath = filepath;
	}
	public String getContractname() {
		return contractname;
	}
	public void setContractname(String contractname) {
		this.contractname = contractname;
	}
	public String getCondition() {
		return condition;
	}
	public void setCondition(String condition) {
		this.condition = condition;
	}
}
